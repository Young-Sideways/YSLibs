#include "../inc/util/fun.h"

#include <stdio.h>
#include <threads.h>

void animate(const char** list, size_t list_size, int latency_ms, int times) {
    const struct timespec ts = {.tv_nsec = latency_ms * 1000000};
    for (int iter = 0; iter < times; iter++) {
        for (size_t i = 0; i < list_size; i++) {
            system("clear");
            puts(list[i]);
            thrd_sleep(&ts, NULL);
        }
    }
}

void animate_reverse(const char** list, int list_size, int latency_ms, int times) {
    const struct timespec ts = {.tv_nsec = latency_ms * 1000000};
    for (int iter = 0; iter < times; iter++) {
        for (int i = 0; i < list_size; i++) {
            system("clear");
            puts(list[i]);
            thrd_sleep(&ts, NULL);
        }
        for (int i = (list_size - 1); i >= 0; i--) {
            system("clear");
            puts(list[i]);
            thrd_sleep(&ts, NULL);
        }
    }
}

